package com.otrip.pojo.ws;

import java.util.List;

public class Favorite {
	
	private String actiId;
	private String actiTitle;
	private String country;
	private String price;
	private String type;
	private List url;
	public String getActiId() {
		return actiId;
	}
	public void setActiId(String actiId) {
		this.actiId = actiId;
	}
	public String getActiTitle() {
		return actiTitle;
	}
	public void setActiTitle(String actiTitle) {
		this.actiTitle = actiTitle;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public List getUrl() {
		return url;
	}
	public void setUrl(List url) {
		this.url = url;
	}
	
	
	

}
