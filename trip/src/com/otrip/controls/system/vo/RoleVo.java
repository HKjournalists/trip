package com.otrip.controls.system.vo;

import javax.persistence.Column;
import javax.persistence.Id;

/**
 * @see 列表查询角色VO
 * @author Administrator
 *
 */
public class RoleVo {

	
	private String fsId;
	private String fsName;
	private String fsSign;
	private String fsStatus;
	private String fsParentId;
	private Integer fnOrder;
	private String fsParentName;
	
	public RoleVo(String fsId, String fsName, String fsSign, String fsStatus,
			String fsParentId, Integer fnOrder, String fsParentName) {
		super();
		this.fsId = fsId;
		this.fsName = fsName;
		this.fsSign = fsSign;
		this.fsStatus = fsStatus;
		this.fsParentId = fsParentId;
		this.fnOrder = fnOrder;
		this.fsParentName = fsParentName;
	}
	public String getFsId() {
		return fsId;
	}
	public void setFsId(String fsId) {
		this.fsId = fsId;
	}
	public String getFsName() {
		return fsName;
	}
	public void setFsName(String fsName) {
		this.fsName = fsName;
	}
	public String getFsSign() {
		return fsSign;
	}
	public void setFsSign(String fsSign) {
		this.fsSign = fsSign;
	}
	public String getFsStatus() {
		return fsStatus;
	}
	public void setFsStatus(String fsStatus) {
		this.fsStatus = fsStatus;
	}
	public String getFsParentId() {
		return fsParentId;
	}
	public void setFsParentId(String fsParentId) {
		this.fsParentId = fsParentId;
	}
	public Integer getFnOrder() {
		return fnOrder;
	}
	public void setFnOrder(Integer fnOrder) {
		this.fnOrder = fnOrder;
	}
	public String getFsParentName() {
		return fsParentName;
	}
	public void setFsParentName(String fsParentName) {
		this.fsParentName = fsParentName;
	}
	
	
 
}
